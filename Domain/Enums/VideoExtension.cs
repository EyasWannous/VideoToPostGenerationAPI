namespace VideoToPostGenerationAPI.Domain.Enums;

/// <summary>
/// Represents various video file extensions.
/// </summary>
public enum VideoExtension
{
    /// <summary>
    /// Indicates no specific video format.
    /// </summary>
    None,

    /// <summary>
    /// WebM video format.
    /// </summary>
    webm,

    /// <summary>
    /// Matroska video format, known for its support of multiple video, audio, and subtitle tracks.
    /// </summary>
    mkv,

    /// <summary>
    /// Flash Video format, often used for online video streaming.
    /// </summary>
    flv,

    /// <summary>
    /// Video Object format, used in DVDs.
    /// </summary>
    vob,

    /// <summary>
    /// Ogg video format.
    /// </summary>
    ogv,

    /// <summary>
    /// Ogg multimedia container format, typically for audio but can be used for video.
    /// </summary>
    ogg,

    /// <summary>
    /// Data Reduced Compression video format.
    /// </summary>
    drc,

    /// <summary>
    /// Graphics Interchange Format video.
    /// </summary>
    gif,

    /// <summary>
    /// GIF video format variant.
    /// </summary>
    gifv,

    /// <summary>
    /// Multiple-image Network Graphics video format.
    /// </summary>
    mng,

    /// <summary>
    /// Audio Video Interleave format, developed by Microsoft.
    /// </summary>
    avi,

    /// <summary>
    /// High-definition video format used in AVCHD camcorders.
    /// </summary>
    MTS,

    /// <summary>
    /// High-definition video format used in AVCHD camcorders, similar to MTS but with different metadata.
    /// </summary>
    M2TS,

    /// <summary>
    /// MPEG transport stream format used for broadcasting and streaming.
    /// </summary>
    TS,

    /// <summary>
    /// QuickTime video format developed by Apple.
    /// </summary>
    mov,

    /// <summary>
    /// QuickTime video format, also developed by Apple.
    /// </summary>
    qt,

    /// <summary>
    /// Windows Media Video format developed by Microsoft.
    /// </summary>
    wmv,

    /// <summary>
    /// YUV video format, often used in video processing.
    /// </summary>
    yuv,

    /// <summary>
    /// RealMedia video format.
    /// </summary>
    rm,

    /// <summary>
    /// RealMedia variable bit rate format.
    /// </summary>
    rmvb,

    /// <summary>
    /// Vivid video format.
    /// </summary>
    viv,

    /// <summary>
    /// Advanced Streaming Format used by Microsoft for streaming media.
    /// </summary>
    asf,

    /// <summary>
    /// Anime Music Video format, often used for fan-made music videos.
    /// </summary>
    amv,

    /// <summary>
    /// MPEG-4 video format, a common format for modern video.
    /// </summary>
    mp4,

    /// <summary>
    /// MPEG-4 Part 14 format, used for video streaming.
    /// </summary>
    m4p,

    /// <summary>
    /// MPEG-4 video format, similar to MP4 but with additional metadata.
    /// </summary>
    m4v,

    /// <summary>
    /// MPEG-1 Video format.
    /// </summary>
    mpg,

    /// <summary>
    /// MPEG-2 Video format.
    /// </summary>
    mp2,

    /// <summary>
    /// MPEG-2 Video format.
    /// </summary>
    mpeg,

    /// <summary>
    /// MPEG-1 Video format, used for video CDs.
    /// </summary>
    mpe,

    /// <summary>
    /// MPEG Video format, used for video CDs and DVDs.
    /// </summary>
    mpv,

    /// <summary>
    /// MPEG-2 Video format, used for professional video recording.
    /// </summary>
    m2v,

    /// <summary>
    /// SubRip Subtitle format used for subtitles.
    /// </summary>
    svi,

    /// <summary>
    /// 3rd Generation Partnership Project video format.
    /// </summary>
    _3gp,

    /// <summary>
    /// 3rd Generation Partnership Project 2 video format.
    /// </summary>
    _3g2,

    /// <summary>
    /// Material Exchange Format used in professional video production.
    /// </summary>
    mxf,

    /// <summary>
    /// ROQ video format, used in video games.
    /// </summary>
    roq,

    /// <summary>
    /// NullSoft Streaming Video format.
    /// </summary>
    nsv,

    /// <summary>
    /// Flash MP4 video format.
    /// </summary>
    f4v,

    /// <summary>
    /// Flash MP4 video format with protection.
    /// </summary>
    f4p,

    /// <summary>
    /// Flash audio format used in video containers.
    /// </summary>
    f4a,

    /// <summary>
    /// Flash video format with binary data.
    /// </summary>
    f4b
}
